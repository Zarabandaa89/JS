//usamos while para repetir una tarea mientras una condición sea verdadera

let diasDeGimnasio = 1 //inicializamos la variable en 1

while(diasDeGimnasio < 7){//mientras la variable sea menor a 7 se ejecutará el código
    console.log(`Felicidades, fuiste ${diasDeGimnasio} al gimnasio`) //imprimimos el mensaje
    diasDeGimnasio++;//incrementamos la variable en 1 para que no sea un bucle infinito
}